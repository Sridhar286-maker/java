public class example3 {
    public static void main(String[] args) {
        int coffee=3;
        switch (coffee)
        {
            case 1 :{
                System.out.println("black coffee");
                break;
                }
            case 2:
                 System.out.println("white coffee");
                 break;
            case 3:
                System.out.println("tea");
                break;
            case 4:
                System.out.println("normal coffee");
                break;
            default :
                System.out.println("there is no coffee");
        }

    }
}
